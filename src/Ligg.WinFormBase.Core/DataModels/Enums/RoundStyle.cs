namespace Ligg.WinFormBase
{
    public enum RoundStyle
    {
        /// 四个角都不是圆角。
        None = 0,
        /// 四个角都为圆角。
        All = 1,
        /// 左边两个角为圆角。
        Left = 2,
        /// 右边两个角为圆角。
        Right = 3,
        /// 上边两个角为圆角。
        Top = 4,
        /// 下边两个角为圆角。
        Bottom = 5,
        /// 左下角为圆角。
        BottomLeft = 6,
        /// 右下角为圆角。
        BottomRight = 7,
    }
}
